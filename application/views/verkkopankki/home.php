<h1>Verkkopankki sivusto</h1>
<p>Nimeni on Riku Kaipainen ja tässä on valmis verkkopankki sivustoni etusivu.<br>
Sivustolla voit testata moderaattorin puolelta tilien hallintaa (Työnalla),
asiakkaana tilin seuraamista, maksujen tekoa sekä pankkikortin toimintoja.<br>
Verkkopankin testaamista varten voit käyttää useampaa eri tiliä jotka tässä listaan.<br>
<br>'käyttäjä', 'salasana'</p>
<ul style="display:inline-table;">
	<li style="disply:inline;">'123456789', '0246'</li>
	<li style="disply:inline;">'gekko', 'dragon'</li>
	<li style="disply:inline;">'0246813579', '1357'</li>
</ul><br>
